class SNClient:
    """Thin wrapper around the ServiceNow Table REST API."""

    HEADERS = {
        "Content-Type": "application/json",
        "Accept": "application/json",
    }

    def __init__(self, instance: str, username: str, password: str,
                 dry_run: bool = False):
        self.base = instance.rstrip("/")
        self.auth = HTTPBasicAuth(username, password)
        self.dry_run = dry_run

    def _url(self, table: str, sys_id: str = "") -> str:
        path = f"/api/now/table/{table}"
        if sys_id:
            path += f"/{sys_id}"
        return self.base + path

    def get(self, table: str, query: str) -> list:
        """Return all records matching sysparm_query."""
        if self.dry_run:
            return []   # Treat everything as "not found" in dry-run
        url = self._url(table)
        resp = requests.get(
            url,
            params={"sysparm_query": query, "sysparm_limit": "10"},
            headers=self.HEADERS,
            auth=self.auth,
            timeout=30,
        )
        resp.raise_for_status()
        return resp.json().get("result", [])

    def create(self, table: str, body: dict) -> dict:
        """Insert a new record and return the created record dict."""
        url = self._url(table)
        if self.dry_run:
            print(f"  [DRY-RUN] POST {url}")
            print(f"            body = {json.dumps(body, indent=12)[:300]}")
            return {"sys_id": "DRY_RUN", "name": body.get("name", "")}
        resp = requests.post(
            url, json=body, headers=self.HEADERS, auth=self.auth, timeout=30
        )
        resp.raise_for_status()
        return resp.json().get("result", {})

    def update(self, table: str, sys_id: str, body: dict) -> dict:
        """Update an existing record (PATCH) and return the updated record."""
        url = self._url(table, sys_id)
        if self.dry_run:
            print(f"  [DRY-RUN] PATCH {url}")
            print(f"            body = {json.dumps(body, indent=12)[:300]}")
            return {"sys_id": sys_id}
        resp = requests.patch(
            url, json=body, headers=self.HEADERS, auth=self.auth, timeout=30
        )
        resp.raise_for_status()
        return resp.json().get("result", {})

    def upsert(self, table: str, query: str, body: dict,
               label: str = "") -> tuple:
        """
        Create or update a record.

        Returns (record_dict, created) where created=True means a new record
        was inserted.
        """
        existing = self.get(table, query)
        label_str = label or body.get("name", table)
        if existing:
            sys_id = existing[0]["sys_id"]
            print(f"  UPDATE  [{table}]  {label_str}  (sys_id: {sys_id})")
            record = self.update(table, sys_id, body)
            return record, False
        else:
            print(f"  CREATE  [{table}]  {label_str}")
            record = self.create(table, body)
            return record, True


