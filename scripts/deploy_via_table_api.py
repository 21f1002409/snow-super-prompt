#!/usr/bin/env python3
"""
deploy_via_table_api.py
=======================
Deploys (or updates) the Snow Super Prompt components directly into a
ServiceNow instance using the REST Table API.

This script is an **alternative** to importing the update set XML.  Use it
when the update set import is not working or when you want to automate
deployments from a CI/CD pipeline.

Components deployed
-------------------
1. Script Include  — SuperPromptProcessor  (sys_script_include)
2. Catalog Item    — AI Super Prompt Executor  (sc_cat_item)
3. Variables       — v_json_payload, v_table_schema, v_existing_records_xml
                     (item_option_new)

Requirements
------------
  Python 3.7+
  pip install requests

Usage
-----
  python deploy_via_table_api.py \\
      --instance  https://dev12345.service-now.com \\
      --username  admin \\
      --password  <password>

  # Use environment variables instead of CLI flags:
  export SN_INSTANCE=https://dev12345.service-now.com
  export SN_USERNAME=admin
  export SN_PASSWORD=<password>
  python deploy_via_table_api.py

Options
-------
  --instance   Base URL of the ServiceNow instance (no trailing slash).
  --username   ServiceNow admin username.
  --password   ServiceNow admin password.
  --scope      Application scope for newly created records (default: global).
  --dry-run    Print what would be done without making any API calls.
"""

