def _resolve_scope_sys_id(client: SNClient, scope_name: str) -> "str | None":
    """Return the sys_id of a sys_scope record, or None if not found."""
    rows = client.get("sys_scope", f"scope={scope_name}")
    if rows:
        return rows[0]["sys_id"]
    # Try by name as a fallback
    rows = client.get("sys_scope", f"name={scope_name}")
    return rows[0]["sys_id"] if rows else None


def deploy_script_include(client: SNClient, scope_sys_id: "str | None") -> str:
    """Deploy (create or update) the SuperPromptProcessor Script Include."""
    print("\n--- Script Include ---")

    script_body = SCRIPT_INCLUDE_PATH.read_text(encoding="utf-8")

    body: dict = {
        "name": "SuperPromptProcessor",
        "api_name": "global.SuperPromptProcessor",
        "active": "true",
        "access": "public",
        "description": (
            "Parses AI Super Prompt JSON payloads and performs the requested "
            "ServiceNow administrative actions."
        ),
        "script": script_body,
    }
    if scope_sys_id:
        body["sys_scope"] = scope_sys_id

    record, _ = client.upsert(
        "sys_script_include",
        "name=SuperPromptProcessor",
        body,
        label="SuperPromptProcessor",
    )
    sys_id = record.get("sys_id", "")
    print(f"         sys_id: {sys_id}")
    return sys_id


def deploy_catalog_item(client: SNClient, scope_sys_id: "str | None") -> str:
    """Deploy (create or update) the AI Super Prompt Executor catalog item."""
    print("\n--- Catalog Item ---")

    description_html = """<p>Use this catalog item to submit a <strong>ServiceNow administrative task</strong>
that was generated by the <em>AI Super Prompt</em>.</p>

<h3>How to use</h3>
<ol>
  <li>Open your AI chatbot (Copilot, ChatGPT, Gemini, etc.).</li>
  <li>Paste the contents of <code>super_prompt.txt</code> to initialise the assistant.</li>
  <li>Describe the ServiceNow change you need in plain English.</li>
  <li>Copy the JSON output produced by the AI.</li>
  <li>Paste it into the <strong>JSON Payload</strong> field below and submit.</li>
</ol>

<p>The attached flow will parse your JSON and automatically perform the requested
change in your ServiceNow instance, then add a work note to this request with
a summary of what was done and links to every record that was affected.</p>"""

    body: dict = {
        "name": CATALOG_ITEM_NAME,
        "short_description": (
            "Submit an AI-generated JSON payload to automate a ServiceNow admin task"
        ),
        "description": description_html,
        "active": "true",
        "sys_class_name": "sc_cat_item",
    }
    if scope_sys_id:
        body["sys_scope"] = scope_sys_id

    record, _ = client.upsert(
        "sc_cat_item",
        f"name={CATALOG_ITEM_NAME}",
        body,
        label=CATALOG_ITEM_NAME,
    )
    sys_id = record.get("sys_id", "")
    print(f"         sys_id: {sys_id}")
    return sys_id


def deploy_variables(
    client: SNClient, cat_item_sys_id: str, scope_sys_id: "str | None"
) -> None:
    """Deploy (create or update) the three catalog item variables."""
    print("\n--- Catalog Item Variables ---")

    for var_def in VARIABLES:
        body: dict = {
            "cat_item": cat_item_sys_id,
            "name": var_def["name"],
            "question_text": var_def["question_text"],
            "label": var_def["label"],
            "type": var_def["type"],
            "mandatory": var_def["mandatory"],
            "order": var_def["order"],
            "help_tag": var_def["help_tag"],
            "active": "true",
        }
        if scope_sys_id:
            body["sys_scope"] = scope_sys_id

        record, _ = client.upsert(
            "item_option_new",
            f"cat_item={cat_item_sys_id}^name={var_def['name']}",
            body,
            label=var_def["name"],
        )
        print(f"         sys_id: {record.get('sys_id', '')}")


# ---------------------------------------------------------------------------
# Summary printer
# ---------------------------------------------------------------------------

def _print_summary(instance: str, si_sys_id: str, ci_sys_id: str) -> None:
    base = instance.rstrip("/")
    print("\n" + "=" * 60)
    print("Deployment complete.  Records affected:")
    print("=" * 60)

    records = [
        ("Script Include", "sys_script_include", si_sys_id),
        ("Catalog Item",   "sc_cat_item",        ci_sys_id),
    ]
    for label, table, sys_id in records:
        if sys_id and sys_id != "DRY_RUN":
            url = f"{base}/{table}.do?sys_id={sys_id}"
        else:
            url = "(dry-run — no sys_id)"
        print(f"  {label:<22}  {table:<25}  {url}")

    print("=" * 60)
    print(
        "\nNext steps:\n"
        "  1. Activate the 'AI Super Prompt Executor Flow' in Flow Designer.\n"
        "  2. Assign the catalog item to a Service Catalog and category.\n"
        "  3. Confirm the Script Include is active (System Definition > Script Includes)."
    )


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Deploy Snow Super Prompt components via ServiceNow Table API.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument(
        "--instance",
        default=os.environ.get("SN_INSTANCE", ""),
        help="ServiceNow instance base URL (env: SN_INSTANCE).",
    )
    parser.add_argument(
        "--username",
        default=os.environ.get("SN_USERNAME", ""),
        help="Admin username (env: SN_USERNAME).",
    )
    parser.add_argument(
        "--password",
        default=os.environ.get("SN_PASSWORD", ""),
        help="Admin password (env: SN_PASSWORD).",
    )
    parser.add_argument(
        "--scope",
        default=os.environ.get("SN_SCOPE", "global"),
        help="Application scope for new records (default: global).",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print planned API calls without executing them.",
    )
    return parser.parse_args()


def main() -> None:
    args = _parse_args()

    # Validate required arguments
    missing = [
        name for name, val in [
            ("--instance / SN_INSTANCE", args.instance),
            ("--username / SN_USERNAME", args.username),
            ("--password / SN_PASSWORD", args.password),
        ]
        if not val
    ]
    if missing:
        sys.exit("ERROR: Missing required arguments:\n  " + "\n  ".join(missing))

    if not SCRIPT_INCLUDE_PATH.exists():
        sys.exit(
            f"ERROR: Script Include source file not found:\n  {SCRIPT_INCLUDE_PATH}"
        )

    if args.dry_run:
        print("[DRY-RUN MODE] No changes will be made to the instance.\n")

    client = SNClient(
        instance=args.instance,
        username=args.username,
        password=args.password,
        dry_run=args.dry_run,
    )

    print(f"Target instance : {args.instance}")
    print(f"Scope           : {args.scope}")

    # Resolve scope sys_id once
    scope_sys_id: "str | None" = None
    if args.scope and args.scope.lower() != "global":
        scope_sys_id = _resolve_scope_sys_id(client, args.scope)
        if not scope_sys_id and not args.dry_run:
            print(
                f"WARNING: Scope '{args.scope}' not found in sys_scope. "
                "Records will be created in the default scope."
            )

    # Deploy components
    si_sys_id = deploy_script_include(client, scope_sys_id)
    ci_sys_id = deploy_catalog_item(client, scope_sys_id)
    deploy_variables(client, ci_sys_id, scope_sys_id)

    _print_summary(args.instance, si_sys_id, ci_sys_id)


if __name__ == "__main__":
    main()
